    private void btnCrearUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearUserActionPerformed
        if(txtNombres.getText().isEmpty() || txtUser.getText().isEmpty() ||
            password.getPassword().length == 0 || confPassword.getPassword().length == 0) {
            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos obligatorios",
                "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Verificar que las contrase침as coincidan
        if(!String.valueOf(password.getPassword()).equals(String.valueOf(confPassword.getPassword()))) {
            JOptionPane.showMessageDialog(this, "Las contrase침as no coinciden",
                "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Crear el objeto Usuario
        Usuario usuario = new Usuario();
        usuario.setNombres(txtNombres.getText());
        usuario.setApellidoP(txtApellidoP.getText());
        usuario.setApellidoM(txtApellidoM.getText());
        usuario.setPuesto(txtPuesto.getText());
        usuario.setRol(comboRol.getSelectedItem().toString());
        usuario.setNombre(txtUser.getText());
        usuario.setContrase침a(String.valueOf(password.getPassword()));

        // Intentar guardar en la base de datos
        UsuarioBD usuarioBD = new UsuarioBD();
        if(usuarioBD.crearUsuario(usuario)) {
            JOptionPane.showMessageDialog(this, "Usuario creado exitosamente",
                "칄xito", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Error al crear el usuario",
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCrearUserActionPerformed

    private void btneliminaruserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminaruserActionPerformed
        // TODO add your handling code here:
        String nombreAEliminar = JOptionPane.showInputDialog(
    this, 
    "Ingrese el nombre de usuario a eliminar:", 
    "", // 游녣 sin t칤tulo
    JOptionPane.PLAIN_MESSAGE
);

    if (nombreAEliminar == null || nombreAEliminar.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debe ingresar un nombre de usuario v치lido.",
            "Advertencia", JOptionPane.WARNING_MESSAGE);
        return;
    }

    UsuarioBD usuarioBD = new UsuarioBD();
    Usuario user = usuarioBD.buscarUsuarioPorNombre(nombreAEliminar);

    if (user == null) {
        JOptionPane.showMessageDialog(this, "El usuario no existe.",
            "Informaci칩n", JOptionPane.INFORMATION_MESSAGE);
    } else if (user.getRol().equalsIgnoreCase("Gerente")) {
        JOptionPane.showMessageDialog(this, "No se puede eliminar a un usuario con rol Gerente.",
            "Advertencia", JOptionPane.WARNING_MESSAGE);
    } else {
        int confirm = JOptionPane.showConfirmDialog(this,
            "쮼st치 seguro de que desea eliminar al usuario: " + nombreAEliminar + "?",
            "Confirmar eliminaci칩n", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            if (usuarioBD.eliminarUsuarioPorNombre(nombreAEliminar)) {
                JOptionPane.showMessageDialog(this, "Usuario eliminado exitosamente.",
                    "칄xito", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar el usuario.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    }//GEN-LAST:event_btneliminaruserActionPerformed


